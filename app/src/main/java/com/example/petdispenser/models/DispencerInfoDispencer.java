package com.example.petdispenser.models;

public class DispencerInfoDispencer {

    int image;
    String name;

    public DispencerInfoDispencer(int image, String name) {
        this.image = image;
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public String getName() {
        return name;
    }

}
